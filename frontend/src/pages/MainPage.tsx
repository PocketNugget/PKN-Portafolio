                    <div
                      className="blog-excerpt"
                      dangerouslySetInnerHTML={{
                        __html:
                          post.html.replace(/<[^>]+>/g, "").slice(0, 200) +
                          (post.html.replace(/<[^>]+>/g, "").length > 200
                            ? "..."
                            : ""),
                      }}
                    />
